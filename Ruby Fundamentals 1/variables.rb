# What are Variables? 
# Variables are like containers where you can store data.
# Dynamically Typed: Ruby is a dynamically typed language, so you don't need to declare a variable's 
# type beforehand. The type is inferred at runtime.


# Types of Variables in Ruby

# 1. Local Variables begin with a lowercase letter or an underscore. Their scope is limited to the block, method, or class in which they are defined.

local_var = "I'm local"
my_name = "Velvet"
my_var = 10

# 2. Constant variables are a special type of variable whose value cannot be changed once assigned. They begin with an uppercase letter.

CONSTANT_VAR = 3.14
PI = 3.14
You assign a value to a variable using = and if you need to reassign a variable, you can simply assign a new value to it.


